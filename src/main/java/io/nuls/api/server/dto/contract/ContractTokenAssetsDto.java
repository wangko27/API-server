package io.nuls.api.server.dto.contract;

import io.nuls.api.entity.ContractTokenAssets;

import java.util.ArrayList;
import java.util.List;

public class ContractTokenAssetsDto {
    private String accountAddress;
    private Long decimals;
    private String amount;

    public ContractTokenAssetsDto(ContractTokenAssets info) {
        this.accountAddress = info.getAccountAddress();
        this.amount = info.getAmount();
    }

    public static List<ContractTokenAssetsDto> parseList(List<ContractTokenAssets> datas){
        List<ContractTokenAssetsDto> list = new ArrayList<>();
        for (ContractTokenAssets data : datas) {
            ContractTokenAssetsDto dto = new ContractTokenAssetsDto(data);
            list.add(dto);
        }
        return list;
    }

    public String getAccountAddress() {
        return accountAddress;
    }

    public void setAccountAddress(String accountAddress) {
        this.accountAddress = accountAddress == null ? null : accountAddress.trim();
    }

    public Long getDecimals() {
        return decimals;
    }

    public void setDecimals(Long decimals) {
        this.decimals = decimals;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount == null ? null : amount.trim();
    }
}